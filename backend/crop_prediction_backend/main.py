"""
Crop Recommendation API — Updated to return Top-5 crops with probabilities.
Deploy this as main.py on the Render crop-prediction service.
"""

import joblib
import numpy as np
from fastapi import FastAPI, HTTPException
from fastapi.middleware.cors import CORSMiddleware
from pydantic import BaseModel, Field
from typing import List

# ── Load model ─────────────────────────────────────────────────────────────

try:
    model = joblib.load("crop_classifier.pkl")
    # Extract class labels (crop names) from the model
    CROP_LABELS: List[str] = list(model.classes_)
    HAS_PROBA = hasattr(model, "predict_proba")
    print(f"Model loaded ✓  |  Classes: {CROP_LABELS}  |  predict_proba: {HAS_PROBA}")
except Exception as e:
    print(f"ERROR loading model: {e}")
    model = None
    CROP_LABELS = []
    HAS_PROBA = False

# ── App ─────────────────────────────────────────────────────────────────────

app = FastAPI(
    title="Crop Recommendation API",
    description="Predicts the top 5 suitable crops based on soil nutrients and weather.",
    version="2.0.0",
)

app.add_middleware(
    CORSMiddleware,
    allow_origins=["*"],
    allow_methods=["*"],
    allow_headers=["*"],
)

# ── Schemas ──────────────────────────────────────────────────────────────────

class CropInput(BaseModel):
    N: float = Field(..., ge=0, le=200, description="Nitrogen (kg/ha)")
    P: float = Field(..., ge=0, le=200, description="Phosphorus (kg/ha)")
    K: float = Field(..., ge=0, le=250, description="Potassium (kg/ha)")
    temperature: float = Field(..., ge=-10, le=60, description="Temperature (°C)")
    humidity: float = Field(..., ge=0, le=100, description="Humidity (%)")
    ph: float = Field(..., ge=0, le=14, description="Soil pH")
    rainfall: float = Field(..., ge=0, le=500, description="Rainfall (mm)")

    class Config:
        json_schema_extra = {
            "example": {
                "N": 90, "P": 42, "K": 43,
                "temperature": 20.87, "humidity": 82.0,
                "ph": 6.5, "rainfall": 202.93,
            }
        }


class CropResult(BaseModel):
    crop: str
    probability: float
    rank: int


class PredictionResponse(BaseModel):
    predicted_crop: str          # top-1 best crop (backward compat)
    confidence_note: str
    predictions: List[CropResult]  # Top-5 with probabilities


# ── Endpoints ────────────────────────────────────────────────────────────────

@app.get("/")
def root():
    return {"message": "Crop Recommendation API v2", "status": "running"}


@app.get("/health")
def health():
    return {"status": "ok", "model_loaded": model is not None}


@app.post("/predict", response_model=PredictionResponse, tags=["Prediction"])
def predict(data: CropInput):
    """
    Returns the top-5 most suitable crops with their probability scores.
    The `predicted_crop` field gives the single best crop for backward compatibility.
    The `predictions` array gives all 5 with confidence values (0.0 – 1.0).
    """
    if model is None:
        raise HTTPException(status_code=503, detail="Model not loaded")

    features = np.array([[
        data.N, data.P, data.K,
        data.temperature, data.humidity,
        data.ph, data.rainfall,
    ]])

    if HAS_PROBA:
        # Get full probability distribution across all crop classes
        probs = model.predict_proba(features)[0]
        # Sort by probability descending
        ranked = sorted(
            zip(CROP_LABELS, probs),
            key=lambda x: x[1],
            reverse=True,
        )
        top5 = ranked[:5]
    else:
        # Fallback for models without predict_proba
        best = model.predict(features)[0]
        top5 = [(best, 1.0)]

    top_crop = top5[0][0]

    predictions = [
        CropResult(crop=name, probability=round(float(prob), 4), rank=i + 1)
        for i, (name, prob) in enumerate(top5)
    ]

    return PredictionResponse(
        predicted_crop=top_crop,
        confidence_note="Top-5 predictions generated by the trained classifier.",
        predictions=predictions,
    )
